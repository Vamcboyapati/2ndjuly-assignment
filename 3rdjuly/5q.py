#(compiled language)
#compiled language is a type of  programming language that Translates entire code into machine code before it runs the programme and creates an executable file that can directly on another simillar system

#pros:

# fast
# code privacy

# cons:

# Not portable

#(Interpreted language)

#Interpreted language is a type of programming language  that translates code line by line during execution.

#pros:

#platform independent(portable)

#cons:

#slow compared to compiled languages

#(Hybrid language)

#hybrid language compile source code to intermediate bytecode ,then interpreted or executed by a virtual maachine.

 #It has both the benifits of compiled and interpreted language.

